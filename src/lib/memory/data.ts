const memoryIndex = new Map<string, MemoryItem>();
for (const mem of MEMORY_ITEMS) {
  memoryIndex.set(mem.id, mem);
}

export function getMemoryById(id: string): MemoryItem | undefined {
  return memoryIndex.get(id);
}

/** Get all connections for a memory (outbound + inbound) */
export function getConnectionsFor(
  id: string
): Array<{ memory: MemoryItem; relationship: RelationshipType; direction: "outbound" | "inbound" }> {
  const results: Array<{ memory: MemoryItem; relationship: RelationshipType; direction: "outbound" | "inbound" }> = [];
  const seen = new Set<string>();

  // Outbound: this memory's explicit connections
  const source = memoryIndex.get(id);
  if (source?.connections) {
    for (const conn of source.connections) {
      const target = memoryIndex.get(conn.targetId);
      if (target && !seen.has(target.id)) {
        seen.add(target.id);
        results.push({ memory: target, relationship: conn.relationship, direction: "outbound" });
      }
    }
  }

  // Inbound: other memories that connect TO this one
  for (const mem of MEMORY_ITEMS) {
    if (mem.id === id || !mem.connections) continue;
    for (const conn of mem.connections) {
      if (conn.targetId === id && !seen.has(mem.id)) {
        seen.add(mem.id);
        results.push({ memory: mem, relationship: conn.relationship, direction: "inbound" });
      }
    }
  }

  return results;
}

export function getConnectionCount(id: string): number {
  return getConnectionsFor(id).length;
}

/**
 * Group memories by domain, returning only domains that have memories.
 */
export function getMemoriesByDomain(): MemoryDomain[] {
  const domainMap = new Map<AgentCategory, MemoryItem[]>();

  for (const mem of MEMORY_ITEMS) {
    const existing = domainMap.get(mem.domain) ?? [];
    existing.push(mem);
    domainMap.set(mem.domain, existing);
  }

  const DOMAIN_ICONS: Record<AgentCategory, string> = {
    "Daily Life": "Sun",
    "Health & Wellness": "Heart",
    Money: "Wallet",
    Relationships: "Users",
    "Life Events": "Flag",
    "Learning & Growth": "BookOpen",
    Work: "Briefcase",
    Creative: "Palette",
  };

  return Array.from(domainMap.entries()).map(([name, memories]) => ({
    name,
    icon: DOMAIN_ICONS[name],
    memories,
  }));
}
